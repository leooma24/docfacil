        <h1 class="text-4xl font-bold mt-4 mb-2">Aviso de Privacidad</h1>
        <p class="text-gray-500 text-sm mb-8">Última actualización: 9 de abril de 2026</p>

        <div class="prose max-w-none space-y-6 text-[15px] leading-relaxed">
            <p>En cumplimiento de los artículos 15 al 18 de la <strong>Ley Federal de Protección de Datos Personales en Posesión de los Particulares</strong> (LFPDPPP) y su Reglamento, ponemos a su disposición el presente Aviso de Privacidad.</p>

            <h2 class="text-2xl font-bold mt-8">1. Identidad del responsable</h2>
            <p><strong>DocFácil</strong> (en adelante "DocFácil" o "la Plataforma"), operado desde México, es el responsable del tratamiento de los datos personales que recabe directamente de sus usuarios (doctores y consultorios que contratan el servicio).</p>
            <p>Para cualquier asunto relacionado con la protección de sus datos personales puede contactarnos en: <strong>privacidad@docfacil.tu-app.co</strong></p>

            <h2 class="text-2xl font-bold mt-8">2. Doble rol: responsable y encargado</h2>
            <p>DocFácil actúa en dos capacidades distintas:</p>
            <ul class="list-disc pl-6 space-y-2">
                <li><strong>Responsable</strong> respecto de los datos de sus usuarios directos (doctores y personal de consultorios).</li>
                <li><strong>Encargado del tratamiento</strong> (art. 50 del Reglamento de la LFPDPPP) respecto de los datos de pacientes que los consultorios cargan en la Plataforma. En este caso, el consultorio es el Responsable y DocFácil únicamente procesa los datos conforme a sus instrucciones.</li>
            </ul>

            <h2 class="text-2xl font-bold mt-8">3. Datos personales que recabamos</h2>
            <p>De los doctores y personal del consultorio: nombre, correo electrónico, teléfono, contraseña (encriptada), cédula profesional, especialidad, datos del consultorio.</p>
            <p>De los pacientes (cargados por el consultorio): nombre, fecha de nacimiento, teléfono, correo, dirección, historial clínico, diagnósticos, tratamientos, recetas, signos vitales, alergias, tipo de sangre, consentimientos firmados, pagos.</p>
            <p class="bg-amber-50 border-l-4 border-amber-400 p-4"><strong>Datos personales sensibles.</strong> La información clínica, médica y de salud es considerada "sensible" por el artículo 3 fracción VI de la LFPDPPP. Su tratamiento requiere <strong>consentimiento expreso y por escrito</strong> del titular.</p>

            <h2 class="text-2xl font-bold mt-8">4. Finalidades del tratamiento</h2>
            <p><strong>Finalidades primarias</strong> (necesarias para prestar el servicio):</p>
            <ul class="list-disc pl-6 space-y-1">
                <li>Brindar acceso a la plataforma de gestión de consultorios.</li>
                <li>Almacenar y gestionar expedientes clínicos, recetas, citas, pagos y consentimientos.</li>
                <li>Enviar notificaciones y recordatorios a pacientes por WhatsApp y correo.</li>
                <li>Procesar cobros de la suscripción del consultorio.</li>
                <li>Cumplir obligaciones legales (NOM-004, retención de expedientes 5 años).</li>
            </ul>
            <p><strong>Finalidades secundarias</strong> (puede oponerse sin afectar el servicio):</p>
            <ul class="list-disc pl-6 space-y-1">
                <li>Envío de información sobre nuevas funciones, promociones y contenido educativo.</li>
                <li>Estadísticas agregadas y anónimas para mejorar la plataforma.</li>
            </ul>
            <p>Si no desea el tratamiento para finalidades secundarias, escriba a privacidad@docfacil.tu-app.co.</p>

            <h2 class="text-2xl font-bold mt-8">5. Transferencias de datos</h2>
            <p>DocFácil no vende ni comparte datos con terceros con fines comerciales. Las únicas transferencias necesarias son con proveedores que operan bajo contratos de confidencialidad:</p>
            <ul class="list-disc pl-6 space-y-1">
                <li>Proveedores de infraestructura (hosting VPS en México)</li>
                <li>Proveedores de envío de correo transaccional</li>
                <li>Proveedores de mensajería WhatsApp Business API</li>
                <li>Pasarelas de pago (Stripe/Mercado Pago) únicamente para cobro de suscripciones</li>
                <li>Autoridades competentes cuando lo requiera la ley</li>
            </ul>

            <h2 class="text-2xl font-bold mt-8">6. Derechos ARCO</h2>
            <p>Usted tiene derecho a Acceder, Rectificar, Cancelar u Oponerse al tratamiento de sus datos personales (derechos ARCO), así como a revocar su consentimiento en cualquier momento.</p>
            <p>Para ejercerlos envíe una solicitud a <strong>privacidad@docfacil.tu-app.co</strong> indicando:</p>
            <ul class="list-disc pl-6 space-y-1">
                <li>Nombre completo y forma de contacto</li>
                <li>Documento que acredite su identidad (INE)</li>
                <li>Descripción clara del derecho que ejerce</li>
            </ul>
            <p>Responderemos en un plazo máximo de <strong>20 días hábiles</strong> conforme al artículo 32 de la LFPDPPP.</p>

            <h2 class="text-2xl font-bold mt-8">7. Medidas de seguridad</h2>
            <p>Hemos implementado medidas administrativas, físicas y técnicas para proteger sus datos contra pérdida, uso indebido, acceso no autorizado o alteración, incluyendo: cifrado TLS en todas las comunicaciones, control de acceso por rol, segregación estricta de datos entre consultorios, respaldos cifrados, y bitácora de auditoría de operaciones sobre datos sensibles.</p>

            <h2 class="text-2xl font-bold mt-8">8. Retención</h2>
            <p>Los expedientes clínicos se conservan durante al menos <strong>5 años</strong> desde el último acto médico, conforme a la NOM-004-SSA3-2012. Los datos fiscales por <strong>5 años</strong> conforme al Código Fiscal de la Federación. Los demás datos mientras subsista la relación contractual más el plazo legal aplicable.</p>

            <h2 class="text-2xl font-bold mt-8">9. Cambios al Aviso</h2>
            <p>Cualquier modificación a este Aviso será notificada mediante la plataforma o correo electrónico, y la versión más reciente estará siempre disponible en esta URL.</p>

            <h2 class="text-2xl font-bold mt-8">10. Autoridad</h2>
            <p>Si considera que su derecho a la protección de datos fue vulnerado, puede acudir al <strong>Instituto Nacional de Transparencia, Acceso a la Información y Protección de Datos Personales (INAI)</strong> — <a href="https://home.inai.org.mx" class="text-teal-600 underline">home.inai.org.mx</a>.</p>
